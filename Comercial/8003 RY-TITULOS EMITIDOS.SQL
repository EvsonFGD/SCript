SELECT 
       Pcprest.CODFILIAL, Pcprest.DUPLIC, Pcprest.PREST,Pcprest.CODCLI,
       Pcprest.VALOR, Pcprest.DTVENC, PCCLIENT.CLIENTE,Pcprest.CODCOB,
       PCPREST.DTEMISSAO, PCCOB.cobranca , pcprest.dtcxmot, :DTINICIO, :DTFIM 
     
FROM PCPREST, PCNFSAID, PCCLIENT, PCCOB
WHERE  
(PCPREST.NUMTRANSVENDA = PCNFSAID.NUMTRANSVENDA)
  AND
   (Pcprest.CODCLI = Pcclient.CODCLI )
  --AND (Pcprest.NUMCAR = :NUMCAR)
  --AND (Pcprest.DTFECHA IS NOT NULL)
 AND (PCPREST.CODCOB NOT IN ('DESD', 'CANC', 'ESTR', 'JUR','CRED')) 
  AND (PCPREST.CODCOB = PCCOB.CODCOB(+))
 AND (PCPREST.DTEMISSAO >= :DTINICIO)
 AND (PCPREST.DTEMISSAO <= :DTFIM)
 AND ((PCPREST.DTESTORNO IS NULL))
-- and (pccob.codcob = :codcob)
ORDER BY PCPREST.CODCOB, PCCLIENT.CLIENTE
